package com.cheuks.bin.original.search;

public class MMX {

	private int idx;
	private String namex;
	private String remarkx;

	public int getIdx() {
		return idx;
	}

	public MMX setIdx(int idx) {
		this.idx = idx;
		return this;
	}

	public String getNamex() {
		return namex;
	}

	public MMX setNamex(String namex) {
		this.namex = namex;
		return this;
	}

	public String getRemarkx() {
		return remarkx;
	}

	public MMX setRemarkx(String remarkx) {
		this.remarkx = remarkx;
		return this;
	}

}
